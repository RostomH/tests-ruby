def echo(string)
  return string
end

def shout(string)
  return string.upcase
end


def repeat(n, string)
  return string * n
end

def start_of_word(string,i)
  return string[0..i-1]
end

def first_word(string)
  return string.split.first
end

def capitalize(string)
  return string.string.scan(/\w+/)
end






